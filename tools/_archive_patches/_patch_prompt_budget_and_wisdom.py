def patch():
    if not PROMPT_PATH.exists():
        print(f"ERROR: {PROMPT_PATH} not found")
        return False

    content = PROMPT_PATH.read_text(encoding="utf-8")
    original_lines = content.count("\n")

    # Safety: check not already patched
    if "СЕКЦІЯ 15" in content:
        print("ALREADY PATCHED: Section 15 already exists")
        return True

    # Create backup
    shutil.copy2(PROMPT_PATH, BACKUP_PATH)
    print(f"Backup: {BACKUP_PATH}")

    # Find insertion point: before the closing """ ``` block
    # The file ends with:
    #   ...final rules...
    #   """
    #   ```
    # We insert before the closing """
    closing_marker = '"""\n```'
    if closing_marker not in content:
        # Try with \r\n
        closing_marker = '"""\r\n```'

    if closing_marker not in content:
        print("ERROR: Could not find closing marker")
        return False

    new_content = content.replace(
        closing_marker,
        SECTION_15 + SECTION_16 + "\n" + closing_marker,
        1,  # only first occurrence
    )

    PROMPT_PATH.write_text(new_content, encoding="utf-8")
    new_lines = new_content.count("\n")
    print(
        f"PATCHED: {original_lines} -> {new_lines} lines (+{new_lines - original_lines})"
    )
    print(f"Added: Section 15 (Budget Intelligence) + Section 16 (Trading Discipline)")

    # Verify
    verify = PROMPT_PATH.read_text(encoding="utf-8")
    checks = [
        "СЕКЦІЯ 15" in verify,
        "СЕКЦІЯ 16" in verify,
        "БЮДЖЕТ ЯК РЕСУРС" in verify,
        "DarkTrader Doctrine" in verify,
        closing_marker in verify,
    ]
    if all(checks):
        print("VERIFY: ALL OK")
        return True
    else:
        print(f"VERIFY FAILED: {checks}")
        # Restore backup
        shutil.copy2(BACKUP_PATH, PROMPT_PATH)
        print("RESTORED from backup")
        return False


if __name__ == "__main__":
    ok = patch()
    raise SystemExit(0 if ok else 1)
